function switchon(){
    document.getElementById("bulb").src="https://www.w3schools.com/html/pic_bulbon.gif";
    document.body.style.backgroundColor="white";
}
function switchoff(){
    document.getElementById("bulb").src="https://www.w3schools.com/html/pic_bulboff.gif";
    document.body.style.backgroundColor="black"
}   